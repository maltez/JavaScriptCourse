/* написать regexp который проверяет номер телефона в формает 
+38(055)-333-22-11 
- скобка может закрываться или нет
+38 может быть или нет, 055 должны быть точно
- или : может быть или нет
потом 3 цифры
- или : может быть или нет
потом 2 цифры
- или : может быть или нет
потом 2 цифры 
*/

let number = /^(\+\d{2})?\(?\d{3}\)?\d{3}\-?\:?\d{2}\-?\:?\d{2}$/g;
let phone = '+380500000000';
console.log(phone.match(number));
